use super::*;

impl Cpu {
    pub(super) fn execute_unofficial_instruction(
        &mut self,
        opcode: u8,
        bus: &mut dyn CpuBus,
    ) -> u8 {
        match opcode {
            0x1A | 0x3A | 0x5A | 0x7A | 0xDA | 0xFA | 0x80 | 0x82 | 0x89 | 0xC2 | 0xE2 | 0x04
            | 0x44 | 0x64 | 0x14 | 0x34 | 0x54 | 0x74 | 0xD4 | 0xF4 | 0x0C | 0x1C | 0x3C | 0x5C
            | 0x7C | 0xDC | 0xFC => self.execute_unofficial_nop(opcode, bus),
            0x0B | 0x2B | 0x4B | 0x6B | 0x8B | 0xCB | 0xEB => {
                self.execute_unofficial_immediate(opcode, bus)
            }
            0xA7 | 0xB7 | 0xAF | 0xBF | 0xA3 | 0xB3 | 0xAB | 0xBB => {
                self.execute_unofficial_load(opcode, bus)
            }
            0x87 | 0x8F | 0x97 | 0x9C | 0x9F | 0x93 | 0x9B | 0x9E => {
                self.execute_unofficial_store(opcode, bus)
            }
            0x07 | 0x03 | 0x0F | 0x13 | 0x17 | 0x1F | 0x1B | 0x43 | 0x4F | 0x53 | 0x57 | 0x5B
            | 0x5F => self.execute_unofficial_shift(opcode, bus),
            0x23 | 0x27 | 0x2F | 0x33 | 0x37 | 0x3B | 0x3F | 0x7F | 0x73 | 0x7B | 0x77 | 0x63
            | 0x67 | 0x6F => self.execute_unofficial_rotate(opcode, bus),
            0xD3 | 0xD7 | 0xDB | 0xDF | 0xC3 | 0xC7 | 0xCF | 0xFB | 0xE3 | 0xF7 | 0xEF | 0xFF
            | 0xE7 | 0xF3 => self.execute_unofficial_inc_dec(opcode, bus),
            0x02 | 0x12 | 0x22 | 0x32 | 0x42 | 0x52 | 0x62 | 0x72 | 0x92 | 0xB2 | 0xD2 | 0xF2 => {
                self.jam()
            }
            _ => {
                log::error!(
                    "Halting on truly unknown opcode: 0x{:02X} at PC: 0x{:04X}",
                    opcode,
                    self.pc.wrapping_sub(1)
                );
                1
            }
        }
    }
}
